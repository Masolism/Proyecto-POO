    private void calcularTriaunguloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularTriaunguloActionPerformed
        // Validate and parse input
        String inputText1 = inputTriangulo1.getText().trim();
        String inputText2 = inputTriangulo2.getText().trim();
        String inputText3 = inputTriangulo3.getText().trim();

        if (inputText1.isEmpty() || inputText2.isEmpty() || inputText3.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese los tres lados del triángulo.", "Error de entrada", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            double lado1 = Double.parseDouble(inputText1);
            double lado2 = Double.parseDouble(inputText2);
            double lado3 = Double.parseDouble(inputText3);

            if (lado1 <= 0 || lado2 <= 0 || lado3 <= 0) {
                JOptionPane.showMessageDialog(this, "Los lados del triángulo deben ser mayores que cero.", "Error de entrada", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Validar que es un triangulo: la suma de dos lados es mayor que la del tercer lado
            if (lado1 + lado2 <= lado3 || lado1 + lado3 <= lado2 || lado2 + lado3 <= lado1) {
                JOptionPane.showMessageDialog(this, "Ups! No se trata de un triángulo. Los lados no cumplen con la desigualdad del triángulo.", "Error de entrada", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Triangulo triangulo = new Triangulo(lado1, lado2, lado3, "Triángulo");

            String resultado = Imprimir.imprimirPerimetro(triangulo);

            resultadoTriangulo.setText(resultado);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingrese números válidos para los lados del triángulo.", "Error de entrada", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_calcularTriaunguloActionPerformed


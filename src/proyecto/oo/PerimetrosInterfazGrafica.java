    private void siguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_siguienteActionPerformed
        int selectedCount = 0;
        if (circuloButton1.isSelected()) {
            selectedCount++;
        }
        if (trianguloButton2.isSelected()) {
            selectedCount++;
        }
        if (cuadradoButton3.isSelected()) {
            selectedCount++;
        }
        if (rectanguloButton4.isSelected()) {
            selectedCount++;
        }
        if (romboButton5.isSelected()) {
            selectedCount++;
        }
        if (romboideButton6.isSelected()) {
            selectedCount++;
        }
        if (trapecioButton7.isSelected()) {
            selectedCount++;
        }
        if (poligonoRegularButton8.isSelected()) {
            selectedCount++;
        }

        if (selectedCount != 1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una figura para calcular el perímetro.", "Error de selección", JOptionPane.ERROR_MESSAGE);
        } else {
            if (circuloButton1.isSelected()) {
                dialogCirculo.setVisible(true);
            } else if (trianguloButton2.isSelected()) {
                dialogTriangulo.setVisible(true);
            } else if (cuadradoButton3.isSelected()) {
                dialogCuadrado.setVisible(true);
            } else if (rectanguloButton4.isSelected()) {
                dialogRectangulo.setVisible(true);
            } else if (romboButton5.isSelected()) {
                dialogRombo.setVisible(true);
            } else if (romboideButton6.isSelected()) {
                dialogRomboide.setVisible(true);
            } else if (trapecioButton7.isSelected()) {
                dialogTrapecio.setVisible(true);
            } else if (poligonoRegularButton8.isSelected()) {
                dialogPoligonoRegular.setVisible(true);
            }
        }

    }//GEN-LAST:event_siguienteActionPerformed


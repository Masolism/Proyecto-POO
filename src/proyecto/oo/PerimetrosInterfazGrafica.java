    private void calcularCirculoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularCirculoButtonActionPerformed

        String inputText = inputCirculo.getText().trim(); // Trim to remove leading/trailing spaces
        if (inputText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese un número para el radio del círculo.", "Error de entrada", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            double radio = Double.parseDouble(inputText);

            if (radio <= 0) {
                JOptionPane.showMessageDialog(this, "El radio del círculo debe ser mayor que cero.", "Error de entrada", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Circulo circulo = new Circulo(radio, "Círculo");

            String resultado = Imprimir.imprimirPerimetro(circulo);

            resultadoCirculo.setText(resultado);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingrese un número válido para el radio del círculo.", "Error de entrada", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_calcularCirculoButtonActionPerformed


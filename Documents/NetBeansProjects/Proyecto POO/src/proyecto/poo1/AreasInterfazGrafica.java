    private void Btn_CalcTrianguloHeronActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_CalcTrianguloHeronActionPerformed
        Triangulo calcular = new Triangulo(0, 0, 0, "Triangulo");
        
        calcular.setLado1(Double.parseDouble(Txt_Lado1TrianguloHeron.getText()));
        calcular.setLado2(Double.parseDouble(Txt_Lado2TrianguloHeron.getText()));
        calcular.setLado3(Double.parseDouble(Txt_Lado3TrianguloHeron.getText()));
        
        if (calcular.getLado1()> 0 && calcular.getLado2() > 0 && calcular.getLado3() > 0){
            Txt_ResultTrianguloHeron.setText(String.valueOf(calcular.calcularAreaHeron()));
        }else{
            JOptionPane.showMessageDialog(null, "¡La longitud no puede ser nula o negativa!");
        }
    }//GEN-LAST:event_Btn_CalcTrianguloHeronActionPerformed

